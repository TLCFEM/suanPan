         integer                 :: NB_ACCESSES_LEFT
         type(LRB_TYPE), pointer :: LRB_PANEL(:)
      END TYPE blr_panel_type
      TYPE diag_block_type
        REAL, POINTER :: DIAG_BLOCK(:)
      END TYPE diag_block_type
